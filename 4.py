def roman_to_int(s: str) -> int:
    # Создаем словарь для соответствия римских цифр и арабских чисел
    roman_to_int_map = {'I': 1, 'V': 5, 'X': 10, 'L': 50, 'C': 100, 'D': 500, 'M': 1000}
    
    # Инициализируем переменную для хранения результата
    result = 0
    
    # Проходим по римской цифре слева направо
    for i in range(len(s)):
        # Если текущая римская цифра меньше следующей, вычитаем ее из результата
        if i < len(s) - 1 and roman_to_int_map[s[i]] < roman_to_int_map[s[i+1]]:
            result -= roman_to_int_map[s[i]]
        # Иначе, прибавляем текущую римскую цифру к результату
        else:
            result += roman_to_int_map[s[i]]
            
    return result

# Получаем от пользователя римскую цифру для преобразования
roman_num = input("Введите римскую цифру: ")

# Преобразуем римскую цифру в арабское число
arabic_num = roman_to_int(roman_num)

# Выводим результат
print(f"Римская цифра '{roman_num}' равна {arabic_num}")